package model.user_management;

public class user_data{
	String username;
	String password;
	
	public void setUsername(String value){
		username = value;
	}
	
	public void setPassword(String value){
		password = value;
	}
	
	public String getUsername(){
		return password;
	}
	
	public String getPassword(){
		return username;
	}
}